namespace proba_1;

public class Hisoblash
{
    public int a = 0;
    public int b = 0;
    
    public Hisoblash(int a, int b)
    {
        this.a = a;
        this.b = b;
    }
    
    public int qoshish()
    {
        return a + b;
    }    
    public int bolish()
    {
        return a / b;
    }
    public int ayirish()
    {
        return a - b;
    }
    public int kopaytirish()
    {
        return a * b;
    }
}